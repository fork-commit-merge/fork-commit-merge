// C++ - Easy 2

#include <iostream>

int main()
{
    int num1, num2, sum;
    
    std::cout << "Enter first number: ";
    std::cin >> num1;
    
    std::cout << "Enter second number: ";
    std::cin >> num2;
    
    sum = num1 + num2;
    
    std::cout << "Sum of " << num1 << " and " << num2 << " is: " << sum << std::endl;
    
    return 0;
}
